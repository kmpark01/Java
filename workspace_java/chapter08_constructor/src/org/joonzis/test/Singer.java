package org.joonzis.test;

public class Singer {
	
	String name;
	
	Song song;
	
	public Singer(String name) {
		super();
		this.name = name;
	}

	void setSong(Song song) {	
		this.song = song;
	}
	
	void output() {
		System.out.println("제목 :" + song.title);
		System.out.println("나라 :" + song.country);
		System.out.println("가수 :" + name);
	} 

}
